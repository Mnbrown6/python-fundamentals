def school_description(value):
    if value == 'E':
        print('Excellent.')
    elif value == 'V':
        print('Very Good.')
    elif value == 'G':
        print('Good Job.')
    elif value == 'A':
        print('Average.')
    elif value == 'F':
        print('Failed.')
    else:
        print('Not a valid grade.')


school_description('a')
















